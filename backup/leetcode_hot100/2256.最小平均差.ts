// @lc code=start
function minimumAverageDifference(nums: number[]): number {
  const left: number[] = new Array(nums.length).fill(0);
  const right: number[] = new Array(nums.length).fill(0);
  const n = nums.length;
  for (let i = 0; i < nums.length; i++) {
    if (i === 0) {
      left[i] = nums[i];
      right[n - 1 - i] = 0;
      continue;
    }
    left[i] = left[i - 1] + nums[i];
    right[n - 1 - i] = nums[n - i] + right[n - i];
  }
  let min = Infinity;
  let ans = 0;
  for (let i = 0; i < n; i++) {
    const R = Math.floor(right[i] / (n - i - 1)) || 0;
    const val = Math.abs(Math.floor(left[i] / (i + 1)) - R);
    if (val < min) {
      min = val;
      ans = i;
    }
  }
  return ans;
}
